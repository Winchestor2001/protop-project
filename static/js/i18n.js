  const DICT = window.I18N_DICT;

  function applyTranslations(root) {
    const nodes = (root || document).querySelectorAll('[data-lang-key]');
    nodes.forEach(el => {
      const k = el.getAttribute('data-lang-key');
      const val = (DICT[current] && DICT[current][k]) || el.getAttribute('data-lang-default');
      if (val) el.textContent = val;
    });
    const placeholders = (root || document).querySelectorAll('[data-lang-key-placeholder]');
    placeholders.forEach(el => {
      const k = el.getAttribute('data-lang-key-placeholder');
      const val = (DICT[current] && DICT[current][k]);
      if (val) el.setAttribute('placeholder', val);
    });
  }

  function bindLangSwitcher() {
    const sel = document.getElementById('lang-switcher');
    if (sel) {
      sel.value = current;
      sel.onchange = () => {
        current = sel.value;
        localStorage.setItem(LS_KEY, current);
        applyTranslations();
      };
    }
  }

  function init() {
    applyTranslations();
    bindLangSwitcher();
  }

  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', init);
  else init();
